exports.Book = class Book {
    constructor(bookId, bookName, isbn, publisher, author, description, pages) {
        this.bookId = bookId;
        this.bookName = bookName;
        this.isbn = isbn;
        this.publisher = publisher;
        this.author = author;
        this.description = description;
        this.pages = pages;
        this.publisher = publisher;
    }
}